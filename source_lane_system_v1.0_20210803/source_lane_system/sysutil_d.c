unsigned short crc16table(unsigned char *ptr, unsigned char len)
{
	unsigned char crchi = 0xff;
	unsigned char crclo = 0xff; 
	unsigned short index;
	while (len--) 
	{
		index = crclo ^ *ptr++; 
		crclo = crchi ^ crctablehi[index];
		crchi = crctablelo[index];
	}
	return (crchi << 8 | crclo);
}



