//read io with timeout or character
int opio_readtv_ch(int fd, char* buf, int n,char ch, int sec, int ms)
{
	int nread,nleft,nrel;
	int ret;
	char *bufp = buf;
    
	fd_set fds,errset;
	struct timeval tv={0,0};

	if((sec <= 0) && (ms<=0))
		return -1;

	if(buf == NULL)
		return -1;


	nleft = n;
	nrel = 0;
	tv.tv_sec = sec;
	tv.tv_usec = 1000*ms;
	
	while(1)
	{
		FD_ZERO(&fds);
		FD_ZERO(&errset);
		FD_SET(fd,&fds);
		FD_SET(fd,&errset);

		switch(ret=select(fd+1,&fds,NULL,&errset,&tv))
		{
			case 0:
				printf("opio_readtv_ch : time out\n");
				return nrel;
			case -1:
				printf("opio_readtv : error\n");
				return -1;
			default:
			{
				if(FD_ISSET(fd,&errset))
				{
					print_error("opio_readtv: error\n");
					return -1;
				}

				if(FD_ISSET(fd, &fds))
				{
					if((nread = read(fd,bufp,nleft)) <= 0)
					{
						if(errno == EINTR)
							nread = 0;
						else
						{
							print_warn("read error errno(%d)\n",errno);
							return -1;
						}
					}
					
					nleft -= nread;
					nrel += nread;
					if(nleft < 0)
					{
						nleft = 0;
					}
					else
						bufp +=nread;
					
					debug_printf("nrel =%d\n",nrel);
					
					if(nleft == 0)
						return nrel;
					if(nrel == 0)
						continue;
					if(buf[nrel-1] == ch)
					{
						return nrel;
					}
				}
			}//end default
		
		}//end switch
	
	}//end while
}

