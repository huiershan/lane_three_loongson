int strncmp_cmd(const char* cmd,const char* line,char ch)
{
	char *p;
	int m,t;

	if((cmd == NULL) || (line == NULL))
		return -2;

	p = (char *)cmd;
	while(*p != '\0')
	{
		if((*p == '\r') || (*p == '\n'))
		{
			*p = ' ';
		}
		else
		{
			p++;
		}
	}

	p = (char *)line;
	while(*p != '\0')
	{
		if((*p == '\r') || (*p == '\n'))
		{
			*p = ' ';
		}
		else
		{
			p++;
		}
	}

	p = strchr(cmd, ch);
	if(p != NULL)
	{
		m = (unsigned long)p - (unsigned long)cmd;
	}
	else
	{
		m = strlen(cmd);
	}

	p = strchr(line, ch);
	if(p != NULL)
	{
		t = (unsigned long)p - (unsigned long)line;
	}
	else
	{
		t = strlen(line);
	}

	if(m != t)
	{
		return -4;
	}

	return strncmp(cmd,line,m);
}

long tim_subtrack(struct timeval *diff,struct timeval x,struct timeval y)
{
	long tim;
	struct timeval rel;
	if(x.tv_sec > y.tv_sec)
	{
		return -1;
	}

	if((x.tv_sec == y.tv_sec)&&(x.tv_usec>y.tv_usec))
	{
		return -1;
	}

	if(y.tv_usec >= x.tv_usec)
	{
		rel.tv_usec = y.tv_usec - x.tv_usec;
		rel.tv_sec = y.tv_sec - x.tv_sec;
	}
	else
	{
		rel.tv_usec = 1*1000*1000 + x.tv_usec - y.tv_usec;
		rel.tv_sec = y.tv_sec - x.tv_sec-1;
	}


	tim = (rel.tv_usec/1000);
	if((rel.tv_usec%1000)>500)
	{
		tim +=1;
	}

	tim += rel.tv_sec*1000;
	if(diff != NULL)
	{
		diff->tv_sec = rel.tv_sec;
		diff->tv_usec = rel.tv_usec;
	}

	return tim;
}

//ms delay
void msleep_m(int msec)
{
	int ts;
	long tu;
	struct timeval tv;
	if(msec <= 0)
	{
		return;
	}
	ts = msec / 1000;
	tu = (msec%1000)*1000;
	tv.tv_sec=ts;
	tv.tv_usec = tu;
	select(0,NULL,NULL,NULL,&tv);
}


